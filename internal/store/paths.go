package store

import (
	"os"
	"path/filepath"
)

// configDir returns the timer-cli config directory, honoring
// $XDG_CONFIG_HOME and falling back to ~/.config on both macOS and Linux.
func configDir() (string, error) {
	base := os.Getenv("XDG_CONFIG_HOME")
	if base == "" {
		home, err := os.UserHomeDir()
		if err != nil {
			return "", err
		}
		base = filepath.Join(home, ".config")
	}
	return filepath.Join(base, "timer-cli"), nil
}

func ensureConfigDir() (string, error) {
	dir, err := configDir()
	if err != nil {
		return "", err
	}
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return "", err
	}
	return dir, nil
}
